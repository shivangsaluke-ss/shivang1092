public class operator{
    public static void main(String[] args) {
        // assignment operator
        int a=7;
        int b=6;
        
        System.out.println(a+=5);
        System.out.println(a-=5);
        System.out.println(a*=5);
        System.out.println(a/=5);
        // logical operator
        System.out.println(a<b && a>b);
        System.out.println(a<b || a>b);
        // ternary
                        


    }
}